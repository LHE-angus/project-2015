package com.ebiz.mmt.domain.functioncharts;

import java.io.Serializable;

import com.ebiz.ssi.domain.BaseDomain;

/**
 * @author Jiang,JiaYong
 */
public class TrendLine extends BaseDomain implements Serializable {

	private static final long serialVersionUID = 1482092324728295042L;

	private String name; // 警示线名称

	private String value; // 警示线数值

	private String color; // 警示线颜色

	private Integer columnColor; // 是否作为柱颜色(0-不作为柱状图的颜色、1-表示如果值大于预警值则柱状图颜色为预警线颜色、-1-表示如果值小于预警值则柱状图颜色为预警线颜色。)

	/**
	 * @val 警示线名称
	 */
	public String getName() {
		return name;
	}

	/**
	 * @val 警示线名称
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @val 警示线数值
	 */
	public String getValue() {
		return value;
	}

	/**
	 * @val 警示线数值
	 */
	public void setValue(String value) {
		this.value = value;
	}

	/**
	 * @val 警示线颜色
	 */
	public String getColor() {
		return color;
	}

	/**
	 * @val 警示线颜色
	 */
	public void setColor(String color) {
		this.color = color;
	}

	/**
	 * @val 是否作为柱颜色(0-不作为柱状图的颜色、1-表示如果值大于预警值则柱状图颜色为预警线颜色、-1-表示如果值小于预警值则柱状图颜色为预警线颜色。)
	 */
	public Integer getColumnColor() {
		return columnColor;
	}

	/**
	 * @val 是否作为柱颜色(0-不作为柱状图的颜色、1-表示如果值大于预警值则柱状图颜色为预警线颜色、-1-表示如果值小于预警值则柱状图颜色为预警线颜色。)
	 */
	public void setColumnColor(Integer columnColor) {
		this.columnColor = columnColor;
	}

}