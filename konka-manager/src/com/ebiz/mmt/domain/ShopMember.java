package com.ebiz.mmt.domain;

import java.io.Serializable;
import java.util.Date;

/**
 * @author Jin,QingHua
 */
public class ShopMember extends UserInfo implements Serializable {

	private static final long serialVersionUID = -1L;

	private Long member_id;

	private Long shop_id;

	private Integer member_type;

	private Date add_date;

	private Long adder_id;

	private Integer state;

	private String shop_name;

	private String memberName;

	private String addName;

	private String user_name;

	private Integer shopNumber;

	public ShopMember() {
	}

	public Long getMember_id() {
		return member_id;
	}

	public void setMember_id(Long member_id) {
		this.member_id = member_id;
	}

	public Long getShop_id() {
		return shop_id;
	}

	public void setShop_id(Long shop_id) {
		this.shop_id = shop_id;
	}

	public Integer getMember_type() {
		return member_type;
	}

	public void setMember_type(Integer member_type) {
		this.member_type = member_type;
	}

	public Date getAdd_date() {
		return add_date;
	}

	public void setAdd_date(Date add_date) {
		this.add_date = add_date;
	}

	public Long getAdder_id() {
		return adder_id;
	}

	public void setAdder_id(Long adder_id) {
		this.adder_id = adder_id;
	}

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

	public String getShop_name() {
		return shop_name;
	}

	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}

	public String getMemberName() {
		return memberName;
	}

	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}

	public String getAddName() {
		return addName;
	}

	public void setAddName(String addName) {
		this.addName = addName;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public Integer getShopNumber() {
		return shopNumber;
	}

	public void setShopNumber(Integer shopNumber) {
		this.shopNumber = shopNumber;
	}

}