package com.ebiz.mmt.domain;

import java.io.Serializable;
import java.math.BigDecimal;

import com.ebiz.ssi.domain.BaseDomain;

/**
 * @author Liu,Huan
 */
public class PdPropertyValue extends BaseDomain implements Serializable {

	private static final long serialVersionUID = -1L;

	private Long id;

	private Short p_type;

	private String fix_value;

	private BigDecimal min_value;

	private BigDecimal max_value;

	private String p_unit;

	public PdPropertyValue() {

	}

	public Short getP_type() {
		return p_type;
	}

	public void setP_type(Short p_type) {
		this.p_type = p_type;
	}

	public String getFix_value() {
		return fix_value;
	}

	public void setFix_value(String fix_value) {
		this.fix_value = fix_value;
	}

	public BigDecimal getMin_value() {
		return min_value;
	}

	public void setMin_value(BigDecimal min_value) {
		this.min_value = min_value;
	}

	public BigDecimal getMax_value() {
		return max_value;
	}

	public void setMax_value(BigDecimal max_value) {
		this.max_value = max_value;
	}

	public String getP_unit() {
		return p_unit;
	}

	public void setP_unit(String p_unit) {
		this.p_unit = p_unit;
	}

	public PdPropertyValue(Long id) {
		this.id = id;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

}