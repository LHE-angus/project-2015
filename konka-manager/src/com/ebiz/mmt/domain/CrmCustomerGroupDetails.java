package com.ebiz.mmt.domain;

import java.io.Serializable;

import com.ebiz.ssi.domain.BaseDomain;


public class CrmCustomerGroupDetails extends BaseDomain implements Serializable {

	private static final long serialVersionUID = -1L;

	private Long id;
	
	private Long headid;
	
	private Long customerid;// r3code 对应的记录所在id
							// .对应的记录所在id,因为系统没有删除.所以这样做可以.但还是sb的做法.
	
	private String customername;//

	private Long deptid;// 客户所在部门id. 一般是分公司
	
	private String remark;
	
	
	public CrmCustomerGroupDetails() {

	}

	public Long getId() {
		return id;
	}
	
	public void setId(Long id) {
		this.id = id;
	}
	
	public Long getHeadid() {
		return headid;
	}
	
	public void setHeadid(Long headid) {
		this.headid = headid;
	}
	
	public Long getCustomerid() {
		return customerid;
	}
	
	public void setCustomerid(Long customerid) {
		this.customerid = customerid;
	}
	
	public String getCustomername() {
		return customername;
	}
	
	public void setCustomername(String customername) {
		this.customername = customername;
	}
	
	public String getRemark() {
		return remark;
	}
	
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	public Long getDeptid() {
		return deptid;
	}
	
	public void setDeptid(Long deptid) {
		this.deptid = deptid;
	}
	
}