package com.ebiz.mmt.domain;

import com.ebiz.ssi.domain.BaseDomain;

/**
 * @author Liu,ZhiXiang
 */
public class EntpShopInfo extends BaseDomain {

	private static final long serialVersionUID = -5939975486884479123L;

	private Long id;

	private Long shop_id;

	private Short content_type;

	private String content;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getShop_id() {
		return shop_id;
	}

	public void setShop_id(Long shop_id) {
		this.shop_id = shop_id;
	}

	public Short getContent_type() {
		return content_type;
	}

	public void setContent_type(Short content_type) {
		this.content_type = content_type;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
}