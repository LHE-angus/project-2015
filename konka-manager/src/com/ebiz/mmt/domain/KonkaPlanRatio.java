package com.ebiz.mmt.domain;

import java.io.Serializable;
import java.math.BigDecimal;

import com.ebiz.ssi.domain.BaseDomain;

/**
 * @author Liu,ZhiXiang
 * @version 2013-7-9
 * @desc 任务系数管理
 */
public class KonkaPlanRatio extends BaseDomain implements Serializable {

	private static final long serialVersionUID = -1L;

	private Long id;
	
	private Integer y;
	
	private String dept_sn;
	
	private String dept_name;
	
	private String fgs_sn;
	
	private BigDecimal ratio;
	
	public KonkaPlanRatio() {

	}

	/**
	 * @val ID
	 */
	public Long getId() {
		return id;
	}
	
	/**
	 * @val ID
	 */
	public void setId(Long id) {
		this.id = id;
	}
	
	/**
	 * @val 年度
	 */
	public Integer getY() {
		return y;
	}
	
	/**
	 * @val 年度
	 */
	public void setY(Integer y) {
		this.y = y;
	}
	
	/**
	 * @val 经办部门编码
	 */
	public String getDept_sn() {
		return dept_sn;
	}
	
	/**
	 * @val 经办部门编码
	 */
	public void setDept_sn(String dept_sn) {
		this.dept_sn = dept_sn;
	}
	
	/**
	 * @val 经办部门名称
	 */
	public String getDept_name() {
		return dept_name;
	}
	
	/**
	 * @val 经办部门名称
	 */
	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}
	
	/**
	 * @val 所属分公司编码
	 */
	public String getFgs_sn() {
		return fgs_sn;
	}
	
	/**
	 * @val 所属分公司编码
	 */
	public void setFgs_sn(String fgs_sn) {
		this.fgs_sn = fgs_sn;
	}
	
	/**
	 * @val 任务系数，指占全部分公司/经办比例
	 */
	public BigDecimal getRatio() {
		return ratio;
	}
	
	/**
	 * @val 任务系数，指占全部分公司/经办比例
	 */
	public void setRatio(BigDecimal ratio) {
		this.ratio = ratio;
	}
	
}