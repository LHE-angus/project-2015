package com.ebiz.mmt.domain.support;

import java.util.List;

/**
 * @author XingXiuDong
 * @date 2012-08-30
 * @des 表格行
 */
public class Tr {
	
	private String trDataType = "0"; // 0或者为空-正常行数据，1-合计数据

	private List<Td> tds; // 

	/**
	 * @val 所有的TD值
	 */
	public List<Td> getTds() {
		return tds;
	}

	/**
	 * @val 0 or null-正常行数据，1-合计数据
	 */
	public String getTrDataType() {
		return trDataType;
	}

	/**
	 * @val 0 or null-正常行数据，1-合计数据
	 */
	public void setTrDataType(String trDataType) {
		this.trDataType = trDataType;
	}

	/**
	 * @val 所有的TD值
	 */
	public void setTds(List<Td> tds) {
		this.tds = tds;
	}

}
